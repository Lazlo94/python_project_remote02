def calcAdd():
    a = int(input("Bitte gib eine Zahl ein: "))
    b = int(input("Gibt eine weitere Zahl ein: "))
    c = a + b

    print(f"Die Summe aus {a} und {b} ist {c}")

calcAdd()

def calcMulti():
    a = int(input("Bitte gib eine Zahl ein: "))
    b = int(input("Gibt eine weitere Zahl ein: "))
    c = a * b

    print(f"Das Produkt aus {a} und {b} ist {c}")

calcMulti()